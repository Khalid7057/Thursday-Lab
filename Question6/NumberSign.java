class NumberSign
{
	 public static void main(String[] args) 
    	{
        		int no=-59;
        		if(no > 0)
        		{
            			System.out.println(no+" is a positive number");
        		}
        		else if(no < 0)
        		{
            			System.out.println(no+" is a negative number");
        		}
        		else
        		{
            			System.out.println(no+" is neither positive nor negative");
        		}
    	}
}